#create the dictionary
dictionary_num = {1:10,2:20,3:30,4:40,5:50,6:60}

#Loop through dictionary to print key and value
#keys
print("Keys :")
for key in dictionary_num.keys():
    print(key)
#values
print("\nValues:")
for value in dictionary_num.values():
    print(value)
#items
print("\nItems(Key-Value pairs):")
for item in dictionary_num.items():
    print(item)
